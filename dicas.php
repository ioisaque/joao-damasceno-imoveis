                        <div id="collapse25" class="accordion-body collapse">
                            <div class="accordion-inner">Se os adquirentes forem casados sob o regime da separação absoluta de bens, não é necessário, pois, neste caso, cada cônjuge tem liberdade para administrar seu patrimônio, inclusive para contrair direitos e obrigações. Porém, nos demais tipos de regime de bens (comunhão total, comunhão parcial e participação final nos aquestos) é necessário o comparecimento do cônjuge uma vez que na celebração de um contrato de compromisso de compra e venda de unidades há diversas obrigações que são contraídas por ambos, como, por exemplo, do recebimento das chaves, concessão de garantias (ex.: hipoteca, Alienação Fiduciária). Também nesses casos, se a aquisição se der por procuração, é necessário que ambos os cônjuges nomeiem procurador através de procuração pública com poderes para a aquisição do imóvel e demais obrigações correlatas.
